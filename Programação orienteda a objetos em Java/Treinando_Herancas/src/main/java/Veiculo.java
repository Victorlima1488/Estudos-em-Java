public class Veiculo {
    private int preco;
    private String cor;
    private boolean estaEmplacado;

    public int getPreco() {
        return preco;
    }

    public void setPreco(int preco) {
        this.preco = preco;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public boolean isEstaEmplacado() {
        return estaEmplacado;
    }

    public void setEstaEmplacado(boolean estaEmplacado) {
        this.estaEmplacado = estaEmplacado;
    }
}
